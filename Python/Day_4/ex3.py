# Str data type
# In Python, A string is a sequence of characters enclosed within a single quote or double quote.
# These characters could be anything like letters, numbers, or special symbols enclosed within double quotation marks

x = 'Python'
# print(type(x))

# <class 'str'>

x1 = '12345'
print(type(x1))
# <class 'str'>

x2 = '2+5j'
print(type(x2))
# <class 'str'>


x3 = "123.90"
print(type(x3))
# <class 'str'>


# string to float:

x4 = float(x3)

print(type(x4))
# <class 'float'>
