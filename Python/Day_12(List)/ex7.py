x = [-2,11,-2,-4,-12,22,4,5,6,7,8,2,3,4,5]

# Ascending order
x.sort()
print(x)
# [-12, -4, -2, -2, 2, 3, 4, 4, 5, 5, 6, 7, 8, 11, 22]


# Desceding order

x.sort(reverse=True)
print(x)
# [22, 11, 8, 7, 6, 5, 5, 4, 4, 3, 2, -2, -2, -4, -12]
x.sort(reverse=False)
print(x)

# [-12, -4, -2, -2, 2, 3, 4, 4, 5, 5, 6, 7, 8, 11, 22]

