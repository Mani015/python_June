
l2 = ['i20','creta','benz','Taxi','Ford','jeep','volvo','mg','Range Rover','Deffender']

print(l2)
# ['i20', 'creta', 'benz', 'Taxi', 'Ford', 'jeep', 'volvo', 'mg', 'Range Rover', 'Deffender']

# extend()	Add the elements of a list (or any iterable), to the end of the current list
# syn: list.extend([])

l2.extend(['RR','Buggati','Audi'])
print(l2)
# ['i20', 'creta', 'benz', 'Taxi', 'Ford', 'jeep', 'volvo', 'mg', 'Range Rover', 'Deffender', 'RR', 'Buggati', 'Audi']

l2.extend(['suziki','herohonda'])
print(l2)


# ['i20', 'creta', 'benz', 'Taxi', 'Ford', 'jeep', 'volvo', 'mg', 'Range Rover', 'Deffender', 'RR', 'Buggati', 'Audi', 'suziki', 'herohonda']


