
# How to find out real value
# x = 'real + imaginary'
x = 10 + 2j
# syntax: variablename.real()

# print(x.real)
# . indicate to represent the current variable name

# 10.0


# To find out imag value
# print(x.imag)
# 2.0


# adding to the real value:
print('before x:',x)
print('after adding',x+2)
# before x: (10+2j)
# after adding (12+2j)

# adding to the imag value

print('before x:',x.imag)
print('after x:',x.imag+4)

# before x: 2.0
# after x: 6.0

print(x)
# (10+2j)
