# In Python everything is a  object

# In Python, the phrase "everything is an object" refers to the fact that every piece of data in Python is represented as an object. This includes not only traditional objects like strings, lists,
# and dictionaries but also fundamental data types like integers, floats, and even functions and modules. Here's what this means:
l1 = [1,2,3,4]
print(type(l1))
# <class 'list'>

a = 20
print(type(a))
# <class 'int'>

b = 12.90
print(type(b))
# <class 'float'>

def FUnction():
    pass

print(type(FUnction))
# <class 'function'>
