# Positional Arguments in Python
# During a function call, values passed through arguments should be in the
# order of parameters in the function definition. This is called positional arguments.
#
# Keyword arguments should follow positional arguments only.

def fun(a,b):
    print(a,b)
fun()
# TypeError: fun() missing 2 required positional arguments: 'a' and 'b'

