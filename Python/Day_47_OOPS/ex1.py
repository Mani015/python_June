a = 10
b = 5
# Special Constructor
# With out operators

print(a.__add__(b))
# 15
print(a.__sub__(b))
# 5
print(a.__mul__(5))
# 50
print(a.__divmod__(b))
# (2,0)
print(a.__pow__(2))
# 100
print(a.__ge__(b))
# True
print(a.__le__(b))
# False

